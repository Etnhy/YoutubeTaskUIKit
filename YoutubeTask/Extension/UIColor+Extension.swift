import Foundation
import UIKit

extension UIColor {
    
    static func buttonGradientStart() -> UIColor {
        return UIColor(named: "ButtonTopColor")!
    }
    
    static func buttonGradientEnd() -> UIColor {
        return UIColor(named: "ButtonBottomColor")!
    }
}
